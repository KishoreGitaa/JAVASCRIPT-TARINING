
// Increment Operator
// Pre-increment
let count = 0
console.log(++count)        
console.log(count)         

// Post-increment
let countnum = 0
console.log(countnum++)       
console.log(countnum)         


// Decrement Operator
// Pre-decrement
let count1 = 0
console.log(--count1)        
console.log(count1)         


// Post-decrement
let countnum1 = 0
console.log(countnum1--)       
console.log(countnum1)         