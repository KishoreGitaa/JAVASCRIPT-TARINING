// Comparison Operators
console.log(3 == '3')          
console.log(3 === '3')          
console.log(3 != 2)             // true, because 3 is not equal to 2
console.log(3 !== '3')         
console.log(3 != 3)             // false, compare only value
console.log(3 > 2)             
console.log(3 < 2)              
console.log(3 >= 2)             
console.log(2 <= 3)   